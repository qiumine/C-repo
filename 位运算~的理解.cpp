#include <stdio.h>
#include <conio.h>

int main()
{
    int a=1;
    printf("%d\n",~a);
      
    getch();
    return 0;
}
/* 计算机的数据全部以补码形式存储 
1、这是1的补码 
  0000 0000    0000 0000   0000 0000  0000 0001  
  全取反之后得到，~a的补码形式， 
  1111 1111    1111 1111   1111 1111  1111 1110      // 这里是补码 
2、对该数进行求原码，减1，除符号位全部取反，得到
  减一 1000 0000    0000 0000   0000 0000  0000 0001
  取反 1000 0000    0000 0000   0000 0000  0000 0010  是-2
  */
